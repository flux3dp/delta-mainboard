int led_table_count_4s = sizeof(led_wave_4s) / sizeof(led_wave_4s[0]);
int led_table_count_5s = sizeof(led_wave_5s) / sizeof(led_wave_5s[0]);
uint32_t shift_count_4s[3] = { 0,0,0 };
uint32_t shift_count_5s[3] = { 0,0,0 };

uint8_t _led_wave_atom_plus_4s(int led_number) {
    //SerialUSB.println(led_hash[led_count-1]);
    shift_count_4s[led_number]++;
    if (shift_count_4s[led_number] >= led_table_count_4s)
        shift_count_4s[led_number] = 0;
    return led_wave_4s[shift_count_4s[led_number]];
}
uint8_t _led_wave_atom_plus_5s(int led_number) {
    //SerialUSB.println(led_hash[led_count-1]);
    shift_count_5s[led_number]++;
    if (shift_count_5s[led_number] >= led_table_count_5s)
        shift_count_5s[led_number] = 0;
    return led_wave_5s[shift_count_5s[led_number]];
}
